package operators;

public class ternaryOperator {

    public static void main(String[] args) {
        
        int ageOfClient = 20;

        // This operation is checking to see if the age 20 (age of client) is over 18. If it is, then return true. Otherwiser, return false.

        boolean isEighteenOrOver = (ageOfClient == 20) ? true : false;

        System.out.println(isEighteenOrOver);

        


    }
}